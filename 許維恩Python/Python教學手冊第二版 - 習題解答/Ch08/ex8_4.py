# ex8_4.py
import pickle
# (a) 找出小於 1000 的所有質數並存放於串列 lst
def is_prime(num):
    if num < 2:
        return False
    for i in range(2, int(num ** 0.5) + 1):
        if num % i == 0:
            return False
    return True

# 小於1000的質數
lst = [num for num in range(2, 1000) if is_prime(num)]
print("(a) 小於 1000 的質數：")     # 顯示找到的質數
print(lst)

# (b) 使用pickle的dumps()將串列轉換為bytes並存入ex8_4.bin 檔案
with open('Ch08\\ex8_4.bin', 'wb') as f:
    pickle_bytes = pickle.dumps(lst)
    f.write(pickle_bytes)
print("(b) 質數列表已經以bytes形式寫入檔案")

# (c) 讀取ex8_4.bin檔案並還原回原始資料
with open('Ch08\\ex8_4.bin', 'rb') as f:
    restored_lst = pickle.loads(f.read())
print("(c) 從檔案中還原回的質數列表：")
print(restored_lst)

