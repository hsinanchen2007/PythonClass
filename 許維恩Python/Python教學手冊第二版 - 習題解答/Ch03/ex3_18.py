# ex3_18.py
a = list(range(0, 5))
print(f"(a) 從0到4的整數串列（含4）: {a}")

b = list(range(2, 10))
print(f"(b) 從2到9的整數串列（含9）: {b}") 

c = list(range(1, 10, 2))
print(f"(c) 從1開始，間隔2，直到9的整數串列（含9）: {c}")

d = list(range(10, 0, -2))
print(f"(d) 從10開始，間隔-2，直到1的整數串列（含1）: {d}")

e = list(range(3, 15, 3))
print(f"(e) 從3開始，每次增加3，直到15（不包含15）的整數串列: {e}")

f = list(range(-5, 6, 2))
print(f"(f) 從-5開始，每次增加2，直到6（不包含6）的整數串列: {f}")

# (g) 建立一個從 20 開始，每次減少 1，直到 11（不包含 11）的整數串列
g = list(range(20, 10, -1))
print(f"(g) 從20開始，每次減少1，直到11（不包含11）的整數串列: {g}")

# (h) list(range(8, 8)) 的結果
h = list(range(8, 8))
print(f"(h) list(range(8, 8)): {h}")

