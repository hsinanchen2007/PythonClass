import numpy as np
a = np.arange(12).reshape(3, 4)
print('a=',a)
# (a) 將陣列 a 拆平成一維陣列
a1 = a.flatten()
print("(a)\n", a1)

# (b) 將陣列 a 重排成 4×3 的二維陣列
b = a.reshape(4, 3)
print("\n(b)\n", b)

# (c) 利用 np.newaxis 增加一個軸，使其成為 3×1×4 的三維陣列
c = a[:, np.newaxis, :]
print("\n(c)\n", c)

# (d) 將 (c) 的結果 copy 一份給 b，並用 squeeze() 將 b 變成 3×4 的二維陣列
b_copy = c.copy()
d = np.squeeze(b_copy)
print("\n(d)\n", d)

# (e) 將陣列 a 水平切割成兩個等份的陣列（每份是 3×2）
e1, e2 = np.hsplit(a, 2)
print("\n(e)\n第一部分：\n", e1, "\n第二部分：\n", e2)

# (f) 沿軸 0 垂直切割成兩部分（1×4 和 2×4）
f1, f2 = np.vsplit(a, [1])
print("\n(f)\n第一部分 (1×4)：\n", f1, "\n第二部分 (2×4)：\n", f2)

