# ex9_12.py
import numpy as np

# 建立陣列 a
a = np.arange(10, 30).reshape(4, 5)
print("原始陣列 a:\n", a)

# (a) array([17, 23])
result_a = np.array([a[1, 2], a[2, 3]])
print("\n(a) array([17, 23])\n", result_a)

# (b) array([10, 16, 23])
result_b = np.array([a[0, 0], a[1, 1], a[2, 3]])
print("\n(b) array([10, 16, 23])\n", result_b)

# (c) array([[13, 14], [23, 24], [28, 29]])
result_c = a[[0, 2, 3], [3, 4, 3]]
result_c = result_c.reshape(3, 1)
result_c = np.hstack([result_c, result_c + 1])  # 往右加一欄
print("\n(c) array([[13, 14], [23, 24], [28, 29]])\n", result_c)

# (d) array([[20, 22, 23], [25, 27, 28]])
result_d = np.array([
    [a[2, 0], a[2, 2], a[2, 3]],
    [a[3, 0], a[3, 2], a[3, 3]]
])
print("\n(d) array([[20, 22, 23], [25, 27, 28]])\n", result_d)

# (e) array([[10, 12], [20, 22]])
result_e = a[:, [0, 2]][[0, 2], :]
print("\n(e) array([[10, 12], [20, 22]])\n", result_e)

# (f) array([[11, 13, 14], [16, 18, 19]])
result_f = np.array([
    [a[0, 1], a[0, 3], a[0, 4]],
    [a[1, 1], a[1, 3], a[1, 4]]
])
print("\n(f) array([[11, 13, 14], [16, 18, 19]])\n", result_f)

