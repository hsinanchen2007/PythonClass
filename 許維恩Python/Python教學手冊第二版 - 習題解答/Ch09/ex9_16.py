import numpy as np
a = np.arange(24).reshape(2, 3, 4)
print('a=',a)
# (a) 在軸 0 位置新增一個軸，使其成為 1×2×3×4 的四維陣列
a1 = a[np.newaxis, :]
print("(a)\n", a1)
print("形狀：", a1.shape)  # (1, 2, 3, 4)

# (b) 將陣列 a 沿軸 0 切成兩個等份
b1, b2 = np.split(a, 2, axis=0)
print("\n(b)\n第一份：\n", b1)
print("第二份：\n", b2)

# (c) 將陣列 a 分割成兩個 2×3×2 的陣列（沿軸2切割）
c1, c2 = np.split(a, 2, axis=2)
print("\n(c)\n第一份：\n", c1)
print("第二份：\n", c2)

# (d) 合併 a[0] 和 a[1]
# 垂直方向合併 (列數加倍)
v_merge = np.vstack((a[0], a[1]))
print("\n(d) 垂直合併 (6×4)：\n", v_merge)
# 水平方向合併 (欄數加倍)
h_merge = np.hstack((a[0], a[1]))
print("水平方向合併 (3×8)：\n", h_merge)

