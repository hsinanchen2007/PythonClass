def r_sum(n):    
    if n == 1:      # 當 n 為 1 時，返回 0，因為沒有乘積項
        return 0    
    else:           # 遞迴情況：計算 1*2 + 2*3 + ... + (n-1)*n
        return (n - 1) * n + r_sum(n - 1)

# 測試：計算 n=5 時的乘積和
n = 5
result = r_sum(n)
print(f"1*2 + 2*3 + ... + {n-1}*{n} = {result}")    # 輸出結果

