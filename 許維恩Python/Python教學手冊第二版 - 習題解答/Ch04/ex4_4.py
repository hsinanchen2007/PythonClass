# 讀取三個邊長
s1 = int(input("請輸入第一個邊長："))
s2 = int(input("請輸入第二個邊長："))
s3 = int(input("請輸入第三個邊長："))

# 檢查是否符合三角形不等式
if s1 + s2 > s3 and s1 + s3 > s2 and s2 + s3 > s1:
    print(f"({s1}, {s2}, {s3}) 可以形成三角形")
else:
    print(f"({s1}, {s2}, {s3}) 無法形成三角形")

'''output--------------------
請輸入第一個邊長：5
請輸入第二個邊長：12
請輸入第三個邊長：3
(5, 12, 3) 無法形成三角形
--------------------------'''