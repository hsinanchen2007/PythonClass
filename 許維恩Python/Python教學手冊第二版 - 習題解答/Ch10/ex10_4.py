# ex10_4.py
import numpy as np
# 建立二維陣列 a
a = np.array([[5, 12, 8, 14],
              [6, 1, 8, 14],
              [21, 17, 3, 0]])
print('二維陣列a=')
print(a)

# (a) 計算 a 的平均值，並設為 m
m = np.mean(a)

# (b) 提取 a 中，大於 m 的元素，並計算平均
greater_than_m = a[a > m]
avg_gt_m = np.mean(greater_than_m)

# (c) 提取 a 中，小於等於 m 的元素，並計算平均
less_equal_m = a[a <= m]
avg_le_m = np.mean(less_equal_m)

# (d) 建立陣列 b，形狀與 a 相同，值為 1 或 0（根據是否大於 m）
b = np.where(a > m, 1, 0)

# 輸出結果
print(f"(a) 陣列 a 的平均值 m：{m}")
print(f"(b) 大於 m 的元素平均值：{avg_gt_m}")
print(f"(c) 小於等於 m 的元素平均值：{avg_le_m}")
print(f"(d) 陣列 b（以 m 為界，大於為 1，否則為 0）：\n{b}")

