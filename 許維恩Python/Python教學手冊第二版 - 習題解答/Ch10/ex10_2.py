import numpy as np
# 設定陣列
a1 = np.array([1.5, 2.6, 3.3])
a2 = np.array([2.2, 2.1, 3.9])
print('a1=',a1)
print('a2=',a2)
# (a) 計算兩個陣列中對應元素的最大值與最小值
max_vals = np.maximum(a1, a2)
min_vals = np.minimum(a1, a2)

# (b) 計算每個元素的正弦與餘弦值
sin_a1 = np.sin(a1)
cos_a1 = np.cos(a1)
sin_a2 = np.sin(a2)
cos_a2 = np.cos(a2)

# (c) 將 a1 的每個元素轉換為小於該數的最大整數
floor_a1 = np.floor(a1)

# (d) 計算 a2 每個元素以10為底的對數值
log10_a2 = np.log10(a2)

# 輸出結果
print("(a) 對應元素的最大值：", max_vals)
print("    對應元素的最小值：", min_vals)
print("(b) a1 的正弦值：", sin_a1)
print("    a1 的餘弦值：", cos_a1)
print("    a2 的正弦值：", sin_a2)
print("    a2 的餘弦值：", cos_a2)
print("(c) a1 的向下取整結果：", floor_a1)
print("(d) a2 的以10為底的對數值：", log10_a2)

