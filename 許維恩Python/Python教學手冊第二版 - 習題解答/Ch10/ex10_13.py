import numpy as np

# 原始陣列
a = np.array([
    [5, 16, 12, 1],
    [6, 11, 18, 13],
    [2, 7,  3,  8]
])

# (a) 將 a 裡的元素從小排到大，排成一個一維陣列
sorted_1d = np.sort(a, axis=None)
print("(a) 排序後的一維陣列：", sorted_1d)

# (b) 將 a 裡的元素在軸1（橫列）的方向由小到大排序
sorted_axis1 = np.sort(a, axis=1)
print("(b) 水平方向由小到大排序：\n", sorted_axis1)

# (c) 將 a 的每一列依該列的第一個元素值由小到大排序
sorted_by_first_col = a[a[:, 0].argsort()]
print("(c) 依每列第一個元素排序後的陣列：\n", sorted_by_first_col)

'''output------------------------------------------------------
(a) 排序後的一維陣列： [ 1  2  3  5  6  7  8 11 12 13 16 18]
(b) 水平方向由小到大排序：
 [[ 1  5 12 16]
 [ 6 11 13 18]
 [ 2  3  7  8]]
(c) 依每列第一個元素排序後的陣列：
 [[ 2  7  3  8]
 [ 5 16 12  1]
 [ 6 11 18 13]]
------------------------------------------------------------'''