# ex10_3.py
import numpy as np
# 設定陣列
a = np.array([67.4, 76.1, 73.7, 84.9])
print('a=',a)

# (a) 將 a 裡的每個元素開根號
sqrt_a = np.sqrt(a)

# (b) 取出 a 中的小數部分
decimal_part = a - np.floor(a)

# (c) 取出整數部分是偶數的元素
even_int_part = a[np.floor(a) % 2 == 0]

# (d) 取出最小元素的索引
min_index = np.argmin(a)

# (e) 提取大於 70 的數並計算平均
greater_than_70 = a[a > 70]
average_gt_70 = np.mean(greater_than_70)

# 輸出結果
print("(a) 每個元素的平方根：", sqrt_a)
print("(b) 每個元素的小數部分：", decimal_part)
print("(c) 整數部分為偶數的元素：", even_int_part)
print("(d) 最小元素的索引：", min_index)
print("(e) 大於 70 的元素平均值：", average_gt_70)

