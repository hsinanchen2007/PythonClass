def find_pi(n):
    pi = 0
    for k in range(n + 1):
        term = (1 / 16**k) * (
            4 / (8 * k + 1)
            - 2 / (8 * k + 4)
            - 1 / (8 * k + 5)
            - 1 / (8 * k + 6))
        pi += term
    return pi

# 測試結果
print(f"n = 2 時，π ≈ {find_pi(2)}")
print(f"n = 10 時，π ≈ {find_pi(10)}")

