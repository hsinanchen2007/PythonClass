# ch9_4.py, 拷貝與檢視的比較
import numpy as np
a = np.array([8, 6, 0, 9, 1, 9])    	# 初始化一維陣列 a
s = a[1:4]   					# 切片取出 [6, 0, 9]，這是 View
s[0] = 99  					# 修改 s[0]，會影響原始陣列 a[1]
print('切片結果 s：', s)  		# s 變為 [99, 0, 9]
print('原始陣列 a：', a)  		# a[1] 被改為 99
 
a = np.array([8, 6, 0, 9, 1, 9])  		# 重新初始化 a 
f = a[[1, 2, 3]]   			# 花式索引取出 [6, 0, 9]，這是 Copy
f[0] = 77  					# 修改 f[0]，不會影響原始陣列 a[1]
print('花式索引結果 f：', f)  	# f 變為 [77, 0, 9]
print('原始陣列 a：', a)  		# a[1] 保持不變