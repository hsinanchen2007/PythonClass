import numpy as np
# 原始陣列
a = np.array([38, 43, 21, 87, 92, 55, 40, 63])
print("原始陣列 a:\n", a)

# (a) array([21, 21, 87, 87])
result_a = a[[2, 2, 3, 3]]
print("\n(a) array([21, 21, 87, 87])\n", result_a)

# (b) array([[21, 21, 87, 87]])
result_b = a[[2, 2, 3, 3]].reshape(1, 4)
print("\n(b) array([[21, 21, 87, 87]])\n", result_b)

# (c) array([[21], [21], [87], [87]])
result_c = a[[2, 2, 3, 3]].reshape(4, 1)
print("\n(c) array([[21], [21], [87], [87]])\n", result_c)

# (d) array([[21, 92, 92], [87, 43, 38]])
result_d = np.array([[a[2], a[4], a[4]],
                     [a[3], a[1], a[0]]])
print("\n(d) array([[21, 92, 92], [87, 43, 38]])\n", result_d)

# (e) array([[21, 92], [87, 43], [55, 87]])
result_e = np.array([[a[2], a[4]],
                     [a[3], a[1]],
                     [a[5], a[3]]])
print("\n(e) array([[21, 92], [87, 43], [55, 87]])\n", result_e)

# (f) array([[38, 63], [38, 63], [38, 63]])
result_f = np.array([[a[0], a[7]],
                     [a[0], a[7]],
                     [a[0], a[7]]])
print("\n(f) array([[38, 63], [38, 63], [38, 63]])\n", result_f)

