import numpy as np
# 初始化陣列 a
a = np.arange(20).reshape((4, 5))
print("原始陣列 a:\n", a)

# (a) 提取陣列 a 中，列索引為0到2，行索引為1到3的元素
result_a = a[0:3, 1:4]
print("\n(a) 列索引0到2、行索引1到3的元素:\n", result_a)

# (b) 提取陣列 a 中，行索引為2和3的元素
# 所以只有索引為2能提取
result_b1 = a[2]  
result_b2 = a[3] 
print("\n(b) 行索引為2的元素:\n", result_b1)
print("    行索引為3的元素:\n", result_b2)

# (c) 提取陣列 a 中，列索引為1到3的元素
result_c = a[:, 1:4]
print("\n(c) 列索引為1到3的元素:\n", result_c)

# (d) 將陣列 a 的直行反向排列（也就是上下翻轉）
result_d = a[::-1, :]
print("\n(d) 將直行反向排列:\n", result_d)

# (e) 取出陣列 a 中，所有可以被3整除的數
result_e = a[a % 3 == 0]
print("\n(e) 所有可以被3整除的數:\n", result_e)

# (f) 找出列與行索引和為偶數的元素，並放入串列
result_f = []
for i in range(a.shape[0]):
    for j in range(a.shape[1]):
        if (i + j) % 2 == 0:
            result_f.append(a[i, j])
print("\n(f) 列與行索引和為偶數的元素:\n", result_f)

# (g) 找出可以被2整除但不能被3整除的元素共有幾個
mask = (a % 2 == 0) & (a % 3 != 0)
result_g = np.sum(mask)
print("\n(g) 可以被2整除但不能被3整除的元素個數:", result_g)

