# 驗證 (a) 6 + 3**2 * 4
result_a = 6 + 3**2 * 4
print("(a) Result:", result_a)

# 驗證 (b) 15 / 3 + 4 * 4 / 2
result_b = 15 / 3 + 4 * 4 / 2
print("(b) Result:", result_b)

# 驗證 (c) 6 > 2 + 5
result_c = 6 > 2 + 5
print("(c) Result:", result_c)

# 驗證 (d) ((10 - 4) * 2) ** 2 // 8
result_d = ((10 - 4) * 2) ** 2 // 8
print("(d) Result:", result_d)

'''output----------
(a) Result: 42
(b) Result: 13.0
(c) Result: False
(d) Result: 18
----------------'''

'''說明--------------------------------------------------------------------------------
依序推導每個表達式的計算結果，並使用 Python 來驗證結果是否正確。
(a)	6 + 3**2 * 4
    這個表達式中有指數運算符 ** 和乘法運算符 *。根據運算順序，指數運算的優先級最高，
    接下來是乘法，最後是加法。
    (1)	3**2 先計算，得到 9。
    (2) 然後 9 * 4 會計算，得到 36。
    (3) 最後 6 + 36 計算，結果是 42。
    所以，計算結果為 42。
(b)	15 / 3 + 4 * 4 / 2
    這個表達式中有除法 / 和乘法 *。根據運算順序，乘除法從左到右執行。
    (1)	15 / 3 計算，得到 5.0（因為除法會得到浮點數）。
    (2) 4 * 4 計算，得到 16。
    (3) 16 / 2 計算，得到 8.0。
    (4) 最後 5.0 + 8.0 計算，得到 13.0。
    所以，計算結果為 13.0。
(c)	6 > 2 + 5
    這是比較運算，> 比較兩個值。根據運算順序，先執行加法 2 + 5，然後再進行比較。
    (1) 2 + 5 計算，得到 7。
    (2) 然後進行 6 > 7 的比較，結果是 False。
    所以，計算結果為 False。
(d)	((10 - 4) * 2) ** 2 // 8
    這個表達式有括號、減法、乘法、指數運算和整數除法。
    根據運算順序，首先執行括號中的運算，然後是乘法和指數運算，最後是整數除法。
    (1) 10 - 4 計算，得到 6。
    (2) 6 * 2 計算，得到 12。
    (3) 12 ** 2 計算，得到 144。
    (4) 144 // 8 計算，得到 18（整數除法）。
    所以，計算結果為 18。
----------------------------------------------------------------------------------'''