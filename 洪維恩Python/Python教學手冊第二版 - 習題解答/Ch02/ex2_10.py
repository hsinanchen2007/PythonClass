x=5; x+=2;
print('(a) x=', x)

x=7; x*=3;
print('(b) x=', x)

x=3; x**=4;
print('(c) x=', x)

x=10; x//=3;
print('(d) x=', x)

'''output----------
(a) x= 7
(b) x= 21
(c) x= 81
(d) x= 3
----------------'''

'''說明------------------------------------------------------------------------------
(a)	x = 5; x += 2
    x = 5：初始化 x 為 5。
    x += 2：這是 x = x + 2，將 x 加 2，變成 x = 5 + 2 = 7。
    結果： x = 7
(b)	x = 7; x *= 3
    x = 7：初始化 x 為 7。
    x *= 3：這是 x = x * 3，將 x 乘以 3，變成 x = 7 * 3 = 21。
    結果： x = 21
(c)	x = 3; x **= 4
    x = 3：初始化 x 為 3。
    x **= 4：這是 x = x ** 4，將 x 的值自乘 4 次，變成 x = 3 ** 4 = 81。
    結果： x = 81
(d) x = 10; x //= 3
    x = 10：初始化 x 為 10。
    x //= 3：這是 x = x // 3，使用整數除法，10 // 3 會得到商的整數部分，即 x = 3。
    結果： x = 3
--------------------------------------------------------------------------------'''