while True:
    try:
        # 直接將用戶輸入的 a 轉換為整數，並捕捉錯誤
        a = input("請輸入第一個整數(或輸入Q結束):")
        if a.lower() == 'q':
            print('程式結束')
            break  # 結束程式
        a = int(a)  # 嘗試將 a 轉換為整數

        # 直接將用戶輸入的 b 轉換為整數，並捕捉錯誤
        b = input("請輸入第二個整數(或輸入Q結束):")
        if b.lower() == 'q':
            print('程式結束')
            break  # 結束程式
        b = int(b)  # 嘗試將 b 轉換為整數

        # 判斷 a 和 b 的大小
        if a < b:
            print(f'{a}小於{b}')
        else:
            print(f'{a}大於或等於{b}')

        print('判別完成')  # 如果沒有錯誤，印出此訊息

    except ValueError:
        # 若無法轉換為整數，捕捉到錯誤並顯示提示訊息
        print('無法判別大小，請重新輸入')

