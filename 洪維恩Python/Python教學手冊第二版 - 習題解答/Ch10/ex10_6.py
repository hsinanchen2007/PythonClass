# ex10_6.py
import numpy as np
# 假設 a 是 6x4 的陣列
shape = (6, 4)

# (a) 將一維索引 [7, 9, 14] 轉為 (列, 行)
flat_indices = [7, 9, 14]
rows, cols = np.unravel_index(flat_indices, shape)
coords = list(zip(rows, cols))

# (b) 將 (列, 行) 索引轉為一維索引
row_indices = [0, 3, 4, 2]
col_indices = [1, 3, 2, 0]
flat_indices_2 = np.ravel_multi_index((row_indices, col_indices), shape)

# 輸出結果
print(f"(a) 一維索引 [7, 9, 14] 對應的 (列, 行) 索引為：{coords}")
print(f"(b) (列, 行) 索引 ([0,3,4,2], [1,3,2,0]) 對應的一維索引為：{flat_indices_2}")

