import numpy as np
# 定義 a 和 b 的點集
a = np.array([[3, 4, 8, 3, 8, 9, 7],
              [8, 3, 5, 0, 3, 2, 3],
              [9, 2, 4, 4, 3, 6, 2]])

b = np.array([[8, 7, 3, 1],
              [5, 4, 4, 2],
              [4, 9, 0, 6]])

# (a) 使用 for 迴圈來計算 7×4 的距離平方表
D1 = np.zeros((7, 4))  # 建立一個 7x4 的距離平方表

for i in range(7):
    for j in range(4):
        D1[i, j] = np.sum((a[:, i] - b[:, j])**2)  # 計算距離的平方

print("(a) 使用 for 迴圈計算的距離平方表：")
print(D1)

# (b) 使用 NumPy 廣播來計算 7×4 的距離平方表
# 利用廣播，首先需要調整 a 和 b 的形狀，然後計算每對點的距離平方
a_reshaped = a.T[:, np.newaxis, :]  # 轉置 a 並擴展維度
b_reshaped = b.T[np.newaxis, :, :]  # 擴展維度，便於廣播

D2 = np.sum((a_reshaped - b_reshaped)**2, axis=2)  # 計算距離平方

print("(b) 使用 NumPy 廣播計算的距離平方表：")
print(D2)

# (c) 計算 4×7 的距離平方表，這樣 D[i,j] 代表為 b 裡行索引為 i 的點到 a 裡行索引為 j 的點之距離平方
D3 = D2.T  # 只需轉置 D2 來得到 4×7 的表格

print("(c) 計算 4×7 的距離平方表：")
print(D3)

